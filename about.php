            <h3 id="website">The Website</h3>

            <h4 id="birth">Birth</h4>

            <p>This website was first launched in 2023/04/10 in Neocities as preparation to launch <a href="tensai-knight/index.php">Kishido Temma fansite</a>, but of course, it had been in production for quite some time before that.</p>

            <p>Ever since I was very young, I've always been interested in web development. My first step was dabbling in Tumblr's custom themes. I don't talk about it much, but even in the advent of centralized, corporate-run social media, I still made personal websites. I tried out all sorts of website builders that it became one of my hobbies - Wix, Weebly, uCoz, Webflow, Jimdo, etc - there are so many I don't even remember them all at this point. I even found Carrd when it was still very young, long before it exploded in popularity. In-between all that, I stumbled upon FC2 Website (I think it started with FC2 Blog), which allowed me to code my own website, in contrast with the previous web builders I mentioned. I also got to know more about Bootstrap from dabbling in Toyhouse's custom templates.</p>

            <p>Due to various reasons, I had to part with my FC2 Website. I had a Neocities website too, but it was more "professional". Then, I settled in Carrd as it had matured in the long time I didn't look at it. I tried all sorts of things, even breaking it. My desire to code my own personal website revived after Twitter's meltdown in late 2022 and stepping into Fediverse. In Fediverse, I found out that people still make personal and fannish websites even now. That fact warmed my heart and motivated me to make my own.</p>

            <p>My knowledge consisted of self-study HTML, CSS, and Bootstrap. In 2023, I decided to join some kind of full-stack web development bootcamp, which introduced me to PHP and MySQL. I don't really enjoy back-end stuff, but what I learned there turned out to be very valuable for my web-making journey. Before, I made websites using web templates created by someone else, but I made this website from scratch. Or should I say, from the remains of my practices and assignments in the bootcamp.</p>

            <h4>Name</h4>

            <p><b>Hako Aiko</b> comes from 2 words: hako (箱, box), and Aiko, my own name. It originated from my side
                Twitter account. I was honestly stuck on the name, but then I remembered hako (箱, box) from my favorite light
                novel, "Utsuro no Hako to Zero no Maria". It fits as that Twitter and this website is pretty much a
                box containing myself - my own little world. Also, my favorite word and what I chose to be my
                "theme" is hakoniwa (箱庭, miniature garden), which contains hako in it too (and in a way, it also
                means "box").</p>

            <p><b>Miniature Garden</b> came from the title of my <a href="https://archiveofourown.org/works/6425740/chapters/14709205">most beloved fanfic</a>. It's stuck with me ever since, becoming a part of myself as well - my "theme", which influenced the creation of the name <b>Hako Aiko</b>. Why do I chose it? Simply because it sounds cool. I first
                heard it a lot from fictional works like doujinshi and HJL’s artworks.</p>

            <p>What kind of impression do you get from hearing miniature garden, outside the actual garden and
                miniature toys? For me, it’s bittersweet. A small prison of happiness. HJL’s artwork has the
                subtitle “lifestyle in a toybox”, and these words struck me, creating this impression.</p>

            <p>I like to use alternative names with similar meaning or feeling, such as <b>Dioramic Garden</b>, in other websites.</p>

